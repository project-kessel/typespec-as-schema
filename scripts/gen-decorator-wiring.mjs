#!/usr/bin/env node
// Scans schema/**/*-provider.ts for exported $-prefixed decorator functions
// and generates src/_provider-decorators.ts with re-exports so TypeSpec can
// resolve them from the package entry point.

import { readdirSync, readFileSync, writeFileSync } from "fs";
import { resolve, relative, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, "..");
const SRC_DIR = resolve(ROOT, "src");
const OUT = resolve(SRC_DIR, "_provider-decorators.ts");

function findProviders(dir, results = []) {
  for (const entry of readdirSync(dir, { withFileTypes: true })) {
    const full = resolve(dir, entry.name);
    if (entry.isDirectory()) findProviders(full, results);
    else if (entry.name.endsWith("-provider.ts")) results.push(full);
  }
  return results;
}

const providers = findProviders(resolve(ROOT, "schema"));
const exports = [];

for (const file of providers) {
  const content = readFileSync(file, "utf-8");
  const regex = /export function (\$\w+)/g;
  let m;
  while ((m = regex.exec(content))) {
    const rel = "./" + relative(SRC_DIR, file).replace(/\.ts$/, ".js");
    exports.push({ fn: m[1], from: rel });
  }
}

const lines = [
  "// AUTO-GENERATED by scripts/gen-decorator-wiring.mjs — do not edit.",
  "// Re-exports provider-defined decorators for TypeSpec resolution.",
  "// Regenerate: make build",
  "",
  ...exports.map(({ fn, from }) => `export { ${fn} } from "${from}";`),
  "",
];

writeFileSync(OUT, lines.join("\n"));
console.log(`gen-decorator-wiring: ${exports.length} decorator(s) → src/_provider-decorators.ts`);
